import UIKit

extension UIFont {
    
    static func progress(size: CGFloat) -> UIFont {
        return UIFont(name: "Progress(RUS BY LYAJKA)", size: size)!
    }
    
    static func oswald(size: CGFloat) -> UIFont {
        return UIFont(name: "Oswald", size: size)!
    }
    
}
